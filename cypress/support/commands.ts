Cypress.Commands.add('homepage', () => {
  cy.visit('http://localhost:3000/');

  cy.contains('Escolha com qual usuário irá utilizar a plataforma').should(
    'be.visible'
  );

  cy.get('[data-testid="user-card"]').should('be.visible').first().click();
});
